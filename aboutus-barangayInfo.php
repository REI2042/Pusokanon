            <p>
                The Barangay was founded as one of the several settlements in the later part of the 15th century. No one exactly knew the date when Barangay Pusok occupied by inhabitants. The village was occupied by the Tribes of Datu Mangtawnas as their Chief. The tribe settled the northern coastline of what is now Barangay Pusok. In the olden times, no one became the Chief of the tribe if they are not intelligent, wise, smart and BUS-OK meaning, robust, sturdy and physically muscular in structure. And one of them is Datu Mangtawnas. Datu Mangtawnas was known for these characteristics, thus, inhabitant members of the tribe called him BUS-OK. Since then, visitors coming from the different neighboring tribes call him BUSOK. And later on, they pronounce it as PUSOK, and that is how Brgy. Pusok got its name.<br><br>

